package operadores_atribuição;

public class exercicio01 {

    public static void main(String[] args) {
         int pontos = 100;
         System.out.println("Pontuacao inicial: " + pontos);
         pontos += 50;
         System.out.println("pontuacao pos-bonus: " + pontos);
         pontos -= 30;
         System.out.println("Pontuacao pos-perda: " + pontos);
        System.out.println("Pontuacao final: " + pontos );
    }
  
}

 

        